class LocationTypeModel {
  String? key;
  String? value;

  LocationTypeModel({this.key, this.value});

  LocationTypeModel.fromJson(Map<String, dynamic> json) {
    key = json['key'];
    value = json['value'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['key'] = key;
    data['value'] = value;
    return data;
  }
}
